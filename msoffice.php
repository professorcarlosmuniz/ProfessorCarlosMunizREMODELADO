        <h4 class="text-center" id="Apresentacao">Microsoft Office 2013</h4><br />
        <p>O Microsoft Office 2013, também chamado de Office 15, é a uma versão da suíte de aplicativos Microsoft Office, lançada a 29 de janeiro de 2013, sucedendo à edição de 2010.</p><br />
        
        
        <h4 class="text-center" id="QuemSouEu">Histórico e desenvolvimento</h4><br />
        <p>Em 30 de janeiro de 2012, a Microsoft lançou uma prévia técnica do Office 15, build 3612,1010. A Microsoft anunciou que uma versão beta pública do Office 15 estaria disponível em meados de julho de 2012.<p>

A versão de acolhimento do Office 15 foi lançada em 16 de Julho de 2012.<p>
		<h4 class="text-center" id="QuemSouEu">Novidades</h4><br />
        <p>O aplicativo deve ganhar novos contornos, ao estilo da interface Metro, presente no Windows 8. O Microsoft Outlook recebeu mudanças mais drásticas, por exemplo, a interface Metro fornece uma nova visualização para as tarefas programadas. O PowerPoint irá incluir mais modelos e efeitos de transição, e o OneNote incluirá uma tela nova. O Excel conta com uma ferramenta de filtragem de dados em um cronograma. No Word, a capacidade de inserção de vídeo e áudio online, bem como a difusão de documentos na Web foram implementadas.<p>

Logo à partida, salta à vista a interface, preparada para tablets e sensível ao toque. Além disso tem funcionalidades Cloud e colaboração integradas, que lhe permite começar no computador, continuar no portátil e terminar em outro dispositivo, através da sincronização no OneDrive ou sharepoint para empresas. A possibilidade de encolher os separadores no topo (ribbon) e também passar para o modo full screen facilita mais o trabalho.<p>

Algumas das principais novidades no Excel 2013:<p>

Flash fill – automaticamente preenche uma coluna com dados, baseado na informação adjacente. Varia em função dos padrões detetados.
Powerview – permite ver informação de uma forma simples e atrativa. Selecione os dados, clique em power view, ajuste e configure como quer apresentar os dados. Pode até adicionar bing maps com informação geográfica relacionada com os dados georreferenciados.<p>
Gráficos sugeridos e formatação – agora adicionar gráficos é ainda mais interessante. Para além de lhe serem sugeridos gráficos recomendados para os dados selecionados, é simples pré visualizar outros. Depois de aceitar o gráfico, surgem três botões para configurar, formatação e outras melhorias.<p>
Timeline – permite filtrar e tratar dados com informação cronológica.<p>
Relationship – quando importar dados para o Excel, onde seja possível estabelecer relações, esta função ficará disponível no separador Data. Podendo criar ligações entre os dados minimizando assim introdução de dados redundantes.<p>
Recommended Pivot Tables – esta opção facilita o processo de inserção de tabelas dinâmicas, sendo sugerida uma opção de acordo com os dados selecionados.
Quick Analysis – basta um clique direito nos dados e esta opção sexy ajuda-nos a formatar condicionalmente, gráficos, realce e outras melhoras.<p>

<h4 class="text-center" id="QuemSouEu">Edições da suíte</h4><br />
        <p>O Office 2013 apresenta mudanças nas edições de pacotes. As versões são:<p>

Office 365 Home Premium: Em alusão aos dias do ano, este pacote contém sete aplicativos, Word, Excel, PowerPoint, OneNote, Outlook, Publisher e Acess. Além dos sete aplicativos a suíte oferece 20 GB de espaço no OneDrive mais 60 minutos mensais gratuitos em ligações para qualquer parte do mundo com o Skype. O diferencial deste pacote para os demais é a licença para até cinco computadores (incluindo também computadores MAC da Apple). Esta licença é vendida como anual e mensal.<p>
Office 2013 Home and Student: Contém apenas quatro aplicativos, Word, Excel, PowerPoint e OneNote. Possui apenas uma licença permanente.<p>
Office 2013 Home and Business: Além de contar com com os quatro aplicativos da edição Home and Student, possui também o Outlook. Também oferece uma licença permanente.<p>
Office 2013 Professional: Possui os mesmo aplicativos do Office 365, porém, com uma licença permanente e sem os serviços do OneDrive (neste caso a capacidade do OneDrive não chega a 20 GB) e Skype.<p>

